-- RANKING PRODUTOS MAIS COMPRADOS 12 MESES EM QUANTIDADE -- COMPRAS
SELECT D1_COD, B1_DESC, SUM(D1_QUANT) D1_QUANT FROM SD1010 
INNER JOIN SB1010 ON
SB1010.D_E_L_E_T_ =''
AND B1_COD = D1_COD
INNER JOIN SF4010 ON
SF4010.D_E_L_E_T_ =''
AND F4_CODIGO = D1_TES
AND F4_DUPLIC = 'S'
WHERE SD1010.D_E_L_E_T_ =''
AND D1_DTDIGIT BETWEEN '20220501' AND '20230430' -- COLOCAR VARIAVEL DE 12 MESES (DDATABASE MES CORRENTE -> 12 MESES PARA TRAS)
GROUP BY D1_COD, B1_DESC
ORDER BY D1_QUANT DESC
