-- QUERY PEDIDOS DE VENDAS EM CARTEIRA PROXIMOS 90 DIAS -- FINANCEIRO
SELECT * 
FROM SC5010 
INNER JOIN SC6010 ON
C5_FILIAL = C6_FILIAL
AND C5_NUM = C6_NUM
AND C6_ENTREG >= '20230326'		-- COLOCAR VARIAVEL PARA CONSIDERAR A DATA INICIAL DOS 90 DIAS
AND C6_ENTREG <= '20230625'		-- COLOCAR VARIAVEL PARA CONSIDERAR A DATA FINAL DOS 90 DIAS
AND C6_BLQ <> 'R'
AND C6_QTDVEN > C6_QTDENT
AND SC6010.D_E_L_E_T_ =''
INNER JOIN SF4010 ON
F4_CODIGO = C6_TES 
AND F4_DUPLIC = 'S'
AND SF4010.D_E_L_E_T_ =''
WHERE C5_TIPO = 'N'
AND SC5010.D_E_L_E_T_ =''