-- QUERY PEDIDOS DE VENDAS REGISTRADOS NO DIA -- FINANCEIRO
SELECT * 
FROM SC5010 
INNER JOIN SC6010 ON
C5_FILIAL = C6_FILIAL
AND C5_NUM = C6_NUM
AND SC6010.D_E_L_E_T_ =''
INNER JOIN SF4010 ON
F4_CODIGO = C6_TES 
AND F4_DUPLIC = 'S'
AND SF4010.D_E_L_E_T_ =''
WHERE C5_EMISSAO = '20230326' -- COLOCAR DDATABASE PARA CONSIDERAR O QUE FOI DIGITADO NO DIA 
AND C5_TIPO = 'N'
AND SC5010.D_E_L_E_T_ =''