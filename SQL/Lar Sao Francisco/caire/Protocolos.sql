SELECT 

F1_FILIAL,
F1_DOC,
F1_SERIE,
(SELECT A2_NOME FROM SA2010 WHERE A2_COD = F1_FORNECE AND F1_LOJA = A2_LOJA AND D_E_L_E_T_=' ') FORNECEDOR,
SF1010.F1_VALBRUT,
SF1010.F1_ICMS,
SF1010.F1_ISS,
SF1010.F1_IRRF,
SF1010.F1_EMISSAO,
SF1010.F1__DATA

FROM SF1010
JOIN SE2010 ON E2_NUM = F1_DOC
AND E2_PREFIXO = F1_SERIE
AND E2_FORNECE = F1_FORNECE
AND E2_LOJA = F1_LOJA
AND SE2010.D_E_L_E_T_=' '

WHERE SF1010.D_E_L_E_T_=' '
AND F1_ISS BETWEEN 0 AND :ISS
AND F1_ICMS BETWEEN 0 AND :ICMS
AND F1_IRRF BETWEEN 0 AND :IRRF

;
