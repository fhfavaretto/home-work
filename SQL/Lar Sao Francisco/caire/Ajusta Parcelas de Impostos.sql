SELECT A.R_E_C_N_O_ RECNO_A, B.R_E_C_N_O_ RECNO_B, A.E2_FILIAL, A.E2_PREFIXO, A.E2_NUM, A.E2_VALOR, A.E2_PARCIR, A.E2_PARCISS, A.E2_PARCPIS, A.E2_PARCINS,
B.E2_PARCELA IMP_PARCELA, B.E2_TIPO IMP_TIPO, B.E2_NATUREZ IMP_NAT, (SELECT ED_DESCRIC FROM SED010 WHERE ED_CODIGO = B.E2_NATUREZ AND SED010.D_E_L_E_T_=' ') IMP_NAT_DESC, 
B.E2_VALOR IMP_VALOR
FROM SE2010 A JOIN SE2010 B ON A.E2_FILIAL||A.E2_PREFIXO||A.E2_NUM||A.E2_PARCELA||A.E2_TIPO||A.E2_FORNECE||A.E2_LOJA 
= B.E2_FILIAL||B.E2_TITPAI AND B.E2_TITPAI <> ' ' AND B.D_E_L_E_T_=' ' AND B.E2_NATUREZ NOT IN ('5102006002') --AND B.E2_NATUREZ = '5102006003' AND A.E2_PARCISS <> B.E2_PARCELA
WHERE A.D_E_L_E_T_=' ' AND A.E2_EMISSAO >= '20200101';



UPDATE SE2010 C SET C.E2_PARCPIS = (SELECT B.E2_PARCELA
FROM SE2010 A JOIN SE2010 B ON A.E2_FILIAL||A.E2_PREFIXO||A.E2_NUM||A.E2_PARCELA||A.E2_TIPO||A.E2_FORNECE||A.E2_LOJA 
= B.E2_FILIAL||B.E2_TITPAI AND B.E2_TITPAI <> ' ' AND B.D_E_L_E_T_=' ' AND B.E2_NATUREZ = '5102006002' AND A.E2_PARCPIS <> B.E2_PARCELA
WHERE A.D_E_L_E_T_=' ' AND A.E2_EMISSAO >= '20200101' AND A.R_E_C_N_O_ = C.R_E_C_N_O_ AND A.E2_FILIAL = C.E2_FILIAL)

WHERE C.R_E_C_N_O_ IN (SELECT A.R_E_C_N_O_
FROM SE2010 A JOIN SE2010 B ON A.E2_FILIAL||A.E2_PREFIXO||A.E2_NUM||A.E2_PARCELA||A.E2_TIPO||A.E2_FORNECE||A.E2_LOJA 
= B.E2_FILIAL||B.E2_TITPAI AND B.E2_TITPAI <> ' ' AND B.D_E_L_E_T_=' ' AND B.E2_NATUREZ = '5102006002' AND A.E2_PARCPIS <> B.E2_PARCELA
WHERE A.D_E_L_E_T_=' ' AND A.E2_EMISSAO >= '20200101');
