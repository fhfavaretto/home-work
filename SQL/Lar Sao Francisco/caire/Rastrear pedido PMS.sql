SELECT
D1_FILIAL FILIAL,
D1_ITEM ITEM, 
D1_TOTAL VALOR,
D1_DOC NOTA,
D1_X_DNATU NATUREZA, 
TO_DATE(D1_EMISSAO,'YYYYMMDD') EMISSAO,
TO_DATE(E5_DATA,'YYYYMMDD') BAIXA, 
E5_BENEF BENEFICIARIO,
E5_BANCO BANCO, 
E5_AGENCIA AGENCIA, 
E5_CONTA CONTA, 
(SELECT AF8_DESCRI FROM AF8010 WHERE AFN_PROJET = AF8_PROJET AND AFN_FILIAL = AF8_FILIAL AND AF8010.D_E_L_E_T_=' ') PROJETO, 
(SELECT AF9_DESCRI FROM AF9010 WHERE AF9_FILIAL = AFN_FILIAL AND AFN_TAREFA = AF9_TAREFA AND AF9010.D_E_L_E_T_=' ' AND ROWNUM = 1) TAREFA
 
FROM SD1010
LEFT JOIN SE5010 ON E5_FILIAL = D1_FILIAL AND E5_NUMERO = D1_DOC AND E5_PREFIXO = D1_SERIE AND E5_CLIFOR = D1_FORNECE AND E5_LOJA = D1_LOJA AND E5_TIPODOC <> 'CM' AND SE5010.D_E_L_E_T_=' ' 
LEFT JOIN AJ7010 ON D1_PEDIDO = AJ7_NUMPC AND D1_ITEMPC = AJ7_ITEMPC AND AJ7_FILIAL = D1_FILIAL AND AJ7010.D_E_L_E_T_=' '
LEFT JOIN AFN010 ON AFN_DOC = D1_DOC AND AFN_SERIE = D1_SERIE AND D1_FORNECE = AFN_FORNEC AND D1_LOJA = AFN_LOJA AND AFN_FILIAL = D1_FILIAL AND D1_ITEM = AFN_ITEM AND AFN010.D_E_L_E_T_=' '
WHERE D1_PEDIDO = '126008' AND SD1010.D_E_L_E_T_ = ' ' AND D1_FILIAL = '11001';

SELECT * FROM SC7010 WHERE C7_NUM = '128426' AND C7_FILIAL = '11051';

SELECT COUNT(DISTINCT D1_DOC) AS QTD FROM SD1010 SD1 WHERE SD1.D1_FILIAL = '11001' AND SD1.D_E_L_E_T_ <> '*' AND SD1.D1_PEDIDO = '126008';