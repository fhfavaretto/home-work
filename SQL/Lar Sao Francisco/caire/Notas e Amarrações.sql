SELECT D1_FILIAL, AFG_NUMSC, AFG010.D_E_L_E_T_ AFG_DELETE, AJ7_NUMPC, AJ7010.D_E_L_E_T_ AJ7_DELETE, D1_DOC, AFN_DOC, AFN.D_E_L_E_T_ AFN_DELETE, TO_DATE(SC7010.C7_EMISSAO,'YYYYMMDD') EMISSAO_PEDIDO, TO_DATE(SD1.D1_EMISSAO,'YYYYMMDD') EMISSAO_NOTA,
(SELECT ED_DESCRIC FROM SED010 WHERE ED_CODIGO = D1__NATURE AND SUBSTR(ED_FILIAL,1,2) = SUBSTR(D1_FILIAL,1,2) AND SED010.D_E_L_E_T_=' ' ) NATUREZA,
(SELECT A2_NOME FROM SA2010 WHERE A2_COD = D1_FORNECE AND A2_LOJA = D1_LOJA AND SUBSTR(A2_FILIAL,1,2) = SUBSTR(D1_FILIAL,1,2) AND SA2010.D_E_L_E_T_=' ' ) FORNECEDOR

FROM SD1010 SD1

LEFT JOIN AJ7010 ON D1_PEDIDO = AJ7_NUMPC 
      AND AJ7_ITEMPC = D1_ITEMPC 
      AND AJ7_FILIAL = D1_FILIAL       

LEFT JOIN AFN010 AFN ON SD1.D1_FILIAL = AFN.AFN_FILIAL
			AND SD1.D1_DOC = AFN.AFN_DOC
			AND SD1.D1_SERIE = AFN.AFN_SERIE
			AND SD1.D1_ITEM = AFN.AFN_ITEM
			AND SD1.D1_FORNECE = AFN.AFN_FORNEC
			AND SD1.D1_LOJA = AFN.AFN_LOJA
			AND SD1.D1_COD = AFN.AFN_COD
			AND SD1.D_E_L_E_T_ = ' '
      
LEFT JOIN SC7010 ON C7_FILIAL = D1_FILIAL
      AND C7_NUM = D1_PEDIDO
      AND C7_ITEM = D1_ITEMPC
      AND SC7010.D_E_L_E_T_=' '
      
LEFT JOIN AFG010 ON AFG_FILIAL = C7_FILIAL
      AND AFG_NUMSC = C7_NUMSC
      AND AFG_ITEMSC = C7_ITEMSC
      --AND AFG010.D_E_L_E_T_=' '
      
WHERE D1_EMISSAO BETWEEN '20170501'AND'20170630' AND SD1.D_E_L_E_T_=' '

GROUP BY 

D1_FILIAL, 
AFG_NUMSC, 
AFG010.D_E_L_E_T_,
AJ7_NUMPC,
AJ7010.D_E_L_E_T_,
D1_DOC,
AFN_DOC, 
AFN.D_E_L_E_T_, 
TO_DATE(SC7010.C7_EMISSAO,'YYYYMMDD'),
TO_DATE(SD1.D1_EMISSAO,'YYYYMMDD'),
D1__NATURE,
D1_FORNECE,
D1_LOJA
;

