SELECT
F1_FILIAL FILIAL,
F1_DOC NOTA_FISCAL,
F1_SERIE SERIE,
E2_PARCELA PARCELA,
A2_NOME FORNECEDOR,
E2_VALOR VALOR_BRUTO,
D1_TES TES,
SUBSTR(F1_EMISSAO,7,2)||'/'||SUBSTR(F1_EMISSAO,5,2)||'/'||SUBSTR(F1_EMISSAO,1,4) EMISSAO,
SUBSTR(E2_VENCTO,7,2)||'/'||SUBSTR(E2_VENCTO,5,2)||'/'||SUBSTR(E2_VENCTO,1,4) VENCIMENTO,
SUBSTR(E2_BAIXA,7,2)||'/'||SUBSTR(E2_BAIXA,5,2)||'/'||SUBSTR(E2_BAIXA,1,4) BAIXA,

SUBSTR(E2_BAIXA,5,2) MES_BAIXA,
SUBSTR(E2_BAIXA,1,4) ANO_BAIXA

FROM SF1010

JOIN SD1010 ON F1_DOC = D1_DOC AND F1_SERIE = D1_SERIE AND F1_FILIAL = D1_FILIAL AND SD1010.D_E_L_E_T_=' ' 

JOIN SA2010 ON A2_COD = F1_FORNECE AND F1_LOJA = A2_LOJA AND SA2010.D_E_L_E_T_=' '

JOIN SE2010 ON E2_PREFIXO = F1_SERIE AND E2_NUM = F1_DOC AND E2_FORNECE = F1_FORNECE AND E2_LOJA = F1_LOJA

WHERE SF1010.D_E_L_E_T_=' ' AND F1_FILIAL = '11051' AND D1_TES IN ('006','007')

GROUP BY F1_FILIAL, F1_DOC, F1_SERIE, E2_PARCELA, A2_NOME, E2_VALOR, D1_TES, F1_EMISSAO,E2_VENCTO, E2_BAIXA
;