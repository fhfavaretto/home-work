
SELECT * FROM SRC010 a
WHERE ROWID <> (SELECT MAX(ROWID)
FROM SRC010  b
WHERE 
a.RC_FILIAL  = b.RC_FILIAL
AND a.RC_MAT  = b.RC_MAT
AND a.RC_PD  = b.RC_PD
AND a.RC_DATA  = b.RC_DATA 
)
AND a.RC_PD IN ('600','601','870','871','872','873','874')

order by RC_FILIAL, RC_MAT, RC_PD;

DELETE FROM SRC010 a
WHERE ROWID <> (SELECT MAX(ROWID)
FROM SRC010  b
WHERE 
a.RC_FILIAL  = b.RC_FILIAL
AND a.RC_MAT  = b.RC_MAT
AND a.RC_PD  = b.RC_PD
AND a.RC_DATA  = b.RC_DATA 
)
AND a.RC_PD IN ('600','601','870','871','872','873','874')
;
