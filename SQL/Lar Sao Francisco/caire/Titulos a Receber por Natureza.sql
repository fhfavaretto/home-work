SELECT 
E1_FILIAL FILIAL,
E1_NUM TITULO,
E1_PARCELA PARCELA,
(SELECT A1_NOME FROM SA1010 WHERE A1_COD = E1_CLIENTE AND A1_LOJA = E1_LOJA AND SA1010.D_E_L_E_T_=' ') CLIENTE,
SUBSTR(E1_EMISSAO,7,2)||'/'||SUBSTR(E1_EMISSAO,5,2)||'/'||SUBSTR(E1_EMISSAO,1,4) EMISSAO,
SUBSTR(E1_VENCTO,7,2)||'/'||SUBSTR(E1_VENCTO,5,2)||'/'||SUBSTR(E1_VENCTO,1,4)  VENCIMENTO,
SUBSTR(E1_BAIXA,7,2)||'/'||SUBSTR(E1_BAIXA,5,2)||'/'||SUBSTR(E1_BAIXA,1,4)  BAIXA,
NVL(EV_VALOR,E1_VALOR) VALOR,
(SELECT ED_DESCRIC FROM SED010 WHERE ED_CODIGO = NVL(EV_NATUREZ,E1_NATUREZ) AND SED010.D_E_L_E_T_=' ') NATUREZA

FROM SE1010 

LEFT JOIN SEV010
ON E1_FILIAL = EV_FILIAL
AND E1_PREFIXO = EV_PREFIXO
AND E1_NUM = EV_NUM
AND E1_CLIENTE = EV_CLIFOR
AND E1_LOJA = EV_LOJA
AND E1_TIPO = EV_TIPO
AND E1_PARCELA = EV_PARCELA
AND EV_RECPAG = 'R'
AND SEV010.D_E_L_E_T_=' '

WHERE E1_FILIAL BETWEEN ' ' AND '11001'
AND E1_EMISSAO BETWEEN '20190101' AND '20190630'
AND E1_VENCTO BETWEEN '20190101' AND '20191231'
AND E1_CLIENTE BETWEEN ' ' AND 'ZZZZZZ'
AND E1_LOJA BETWEEN ' ' AND 'ZZ'
AND NVL(EV_NATUREZ,E1_NATUREZ) BETWEEN ' ' AND 'ZZZZZZZZZZ'
AND E1_PARCELA <> '00'
AND E1_FATURA <> 'NOTFAT'

ORDER BY 1,4,5 ;
