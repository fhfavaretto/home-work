SELECT D1_FILIAL FILIAL,
D1_DOC NOTA_FISCAL,
D1_SERIE SERIE,
D1_FORNECE COD_FORNECEDOR,
D1_LOJA LOJA_FORNECEDOR,
(SELECT A2_NOME FROM SA2010 WHERE A2_COD = D1_FORNECE AND A2_LOJA = D1_LOJA AND SA2010.D_E_L_E_T_=' ' AND ROWNUM = 1) FORNECEDOR,
SUBSTR(D1_EMISSAO,7,2)||'/'||SUBSTR(D1_EMISSAO,5,2)||'/'||SUBSTR(D1_EMISSAO,1,4) EMISSAO,
SUBSTR(D1_EMISSAO,7,2) DIA_EMISSAO,
SUBSTR(D1_EMISSAO,5,2) MES_EMISSAO,
SUBSTR(D1_EMISSAO,1,4) ANO_EMISSAO,
ROUND(SUM(D1_TOTAL),2) VALOR
FROM SD1010
WHERE D1_PEDIDO = ' ' /*AND D1_FILIAL = '11051' AND D1_EMISSAO BETWEEN '20151101'AND'20151130'*/
GROUP BY D1_FILIAL, D1_DOC, D1_SERIE, D1_FORNECE, D1_LOJA, D1_EMISSAO;

